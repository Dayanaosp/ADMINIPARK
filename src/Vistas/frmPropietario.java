    // Mostrar los propietarios en la tabla
    void mostrar(String buscar) {
        try {
            DefaultTableModel modelo;
            fPropietario func = new fPropietario(); // Usar fPropietario en lugar de fVehiculo
            modelo = func.mostrar(buscar); // Mostrar propietarios, no vehículos

            // Cambiar columnas para mostrar datos del propietario
            // Suponiendo que las columnas son ID, Nombre, Celular, y Correo
            String[] titulos = {"ID Propietario", "Nombre Completo", "Número Celular", "Correo Electrónico"};
            tablaListado.setModel(new DefaultTableModel(null, titulos));
            
            tablaListado.setModel(modelo); // Establecer el modelo de la tabla
            ocultar_columnas(); // Llamar para ocultar las columnas necesarias
            lblTotalRegistros.setText("Total registros " + Integer.toString(func.totalRegistros)); // Mostrar el total de registros
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage()); // Manejar el error
        }
    }


