    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // Validar que los campos no estén vacíos
        if (txtnombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            txtnombre.requestFocus(); // Enfocar el campo de texto
            return; // Salir del método
        }

        if (txtnumero.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El número celular no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            txtnumero.requestFocus(); // Enfocar el campo de texto
            return; // Salir del método
        }

        if (txtcorreo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El correo electrónico no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            txtcorreo.requestFocus(); // Enfocar el campo de texto
            return; // Salir del método
        }

        vPropietario propietario = new vPropietario(0, txtnombre.getText(), txtnumero.getText(), txtcorreo.getText());

        fPropietario func = new fPropietario();
        if (func.insertar(propietario)) {
            JOptionPane.showMessageDialog(this, "Propietario agregado exitosamente");
            mostrar(""); // Actualiza la tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al agregar propietario");
        }
    }//GEN-LAST:event_btnagregarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
      // Editar el propietario seleccionado
        vPropietario propietario = new vPropietario();

        // Usa la variable de instancia para establecer el ID
        propietario.setIdPropietario(idPropietario); // Usa la variable de instancia
        propietario.setNombreCompleto(txtnombre.getText());
        propietario.setNumeroCelular(txtnumero.getText());
        propietario.setCorreoElectronico(txtcorreo.getText());

        fPropietario func = new fPropietario();
        if (func.editar(propietario)) {
            JOptionPane.showMessageDialog(this, "Propietario editado exitosamente");
            mostrar(""); // Actualiza la tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al editar propietario");
        }
    }//GEN-LAST:event_btneditarActionPerformed

