    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed

    // Validar que los campos no estén vacíos
    if (txtLogin.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "El nombre de usuario no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
        txtLogin.requestFocus(); // Enfocar el campo de texto
        return; // Salir del método
    }

    if (txtPassword.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "La contraseña no puede estar vacía", "Error", JOptionPane.ERROR_MESSAGE);
        txtPassword.requestFocus(); // Enfocar el campo de texto
        return; // Salir del método
    }

    // Crear un nuevo objeto vTrabajador
    vTrabajador trabajador = new vTrabajador();
    trabajador.setNombreUsuario(txtLogin.getText());
    trabajador.setContrasena(txtPassword.getText());
    
    // Convertir el valor seleccionado a la enumeración correspondiente
    trabajador.setRol(vTrabajador.Rol.valueOf(cmbAcceso.getSelectedItem().toString().toUpperCase()));
    trabajador.setEstado(vTrabajador.Estado.valueOf(cmbEstado.getSelectedItem().toString().toUpperCase()));

    fTrabajador func = new fTrabajador();
    if (func.insertar(trabajador)) {
        JOptionPane.showMessageDialog(this, "Trabajador agregado exitosamente");
        mostrar(""); // Actualiza la tabla
    } else {
        JOptionPane.showMessageDialog(this, "Error al agregar trabajador");
    }
    }//GEN-LAST:event_btnagregarActionPerformed

