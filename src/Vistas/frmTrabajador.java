    private void tablaListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListadoMouseClicked
        // Cambia el texto del botón a "Editar"
    btneditar.setText("Editar");
    accion = "editar"; // Establece la acción a editar
    
    // Obtiene la fila seleccionada
    int fila = tablaListado.rowAtPoint(evt.getPoint());
    
    // Captura el ID del trabajador y lo almacena en la variable de instancia
    idTrabajador = Integer.parseInt(tablaListado.getValueAt(fila, 0).toString());
    
    // Captura el nombre de usuario
    txtLogin.setText(tablaListado.getValueAt(fila, 1).toString());
    
    // Captura el estado
    cmbEstado.setSelectedItem(tablaListado.getValueAt(fila, 4).toString()); // Asegúrate de que el índice sea correcto
    
    // Captura el rol
    cmbAcceso.setSelectedItem(tablaListado.getValueAt(fila, 3).toString()); // Asegúrate de que el índice sea correcto
    }//GEN-LAST:event_tablaListadoMouseClicked

